    public static void addComponent(Table t){
        TableComboBox.addItem(t.name);
        AComboBox.addItem(t.name);
        BComboBox.addItem(t.name);
    }
    
    public static void replaceItems(){
        TableComboBox.removeAllItems();
        AComboBox.removeAllItems();
        BComboBox.removeAllItems();
        for (int i = 0; i < GUI.getBase(dbInd).tableNum(); i++){
            TableComboBox.addItem(GUI.getBase(dbInd).getTable(i).name);
            AComboBox.addItem(GUI.getBase(dbInd).getTable(i).name);
            BComboBox.addItem(GUI.getBase(dbInd).getTable(i).name);
        }
    }
    
    public static void removeComponent(String name){
        for (int i = 0; i < GUI.getBase(dbInd).tableNum(); i++)
            if (GUI.getBase(dbInd).getTable(i).name.equals(name)){
                TableComboBox.remove(i);
                AComboBox.remove(i);
                BComboBox.remove(i);
                break;
            }
    }
    
    public static void disableGUI(boolean contr){
        isGUIActive = contr;
    }
