    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        // TODO add your handling code here:
        if (isGUIActive){
            InfoPane.setText(inp + "\n\nОдночасно можна відкрити тільки одну таблицю!!");
        }
        else if (GUI.getBase(dbInd).tableNum() == 0){
            InfoPane.setText(inp + "\n\nБаза має містити хоча б одну таблицю!");
        }
        else{
            TableComboBox.removeAllItems();
            AComboBox.removeAllItems();
            BComboBox.removeAllItems();
            validate();
            repaint();
            GUI.getBase(dbInd).delT(curTable.getIndex());
            for (int i = 0; i < GUI.getBase(dbInd).tableNum(); i++){
                TableComboBox.addItem(GUI.getBase(dbInd).getTableInOrder(i).name);
                AComboBox.addItem(GUI.getBase(dbInd).getTableInOrder(i).name);
                BComboBox.addItem(GUI.getBase(dbInd).getTableInOrder(i).name);
            }
            validate();
            repaint();
        }
    }//GEN-LAST:event_DeleteButtonActionPerformed

    private void TableComboBoxItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_TableComboBoxItemStateChanged
        // TODO add your handling code here:
        if (evt.getStateChange() == ItemEvent.SELECTED){
            String temp = TableComboBox.getSelectedItem().toString();
            for (int i = 0; i < GUI.getBase(dbInd).tableNum(); i++)
                if (GUI.getBase(dbInd).getTableInOrder(i).name.equals(temp)){
                    curTable = GUI.getBase(dbInd).getTableInOrder(i);
                    break;
                }
            InfoPane.setText(inp + "\n\nТаблиця\nНазва: " + curTable.name + "\nКількість рядків: " + curTable.rowNum()
                    + "\nКількість стовпців: " + curTable.attrNum() + "\nНомер: " + curTable.getIndex());
        }
    }//GEN-LAST:event_TableComboBoxItemStateChanged

