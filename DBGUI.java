    private void MultButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MultButtonActionPerformed
        // TODO add your handling code here:
        if (AComboBox.getSelectedItem() == null || BComboBox.getSelectedItem() == null)
            InfoPane.setText(inp + "\n\nБаза має містити хоча б дві таблиці!");
        else{
            int fst = 0;
            int snd = 0;
            for (int i = 0; i < GUI.getBase(dbInd).tableNum(); i++){
                if (GUI.getBase(dbInd).getTableInOrder(i).name.equals(AComboBox.getSelectedItem()))
                    fst = GUI.getBase(dbInd).getTableInOrder(i).getIndex();
                if (GUI.getBase(dbInd).getTableInOrder(i).name.equals(BComboBox.getSelectedItem()))
                    snd = GUI.getBase(dbInd).getTableInOrder(i).getIndex();
            }
            if (fst == snd){
                InfoPane.setText(inp + "\n\nТаблиці мають бути різними!");
            }
            else{
                Table tab = GUI.getBase(dbInd).union(fst, snd);
                addComponent(tab);
                validate();
                setVisible(true);
            }
        }
    }//GEN-LAST:event_MultButtonActionPerformed

    private void GoToButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GoToButtonActionPerformed
        // TODO add your handling code here:
        if (isGUIActive){
            InfoPane.setText(inp + "\n\nОдночасно можна відкрити тільки одну таблицю!!");
        }
        else if (GUI.getBase(dbInd).tableNum() == 0){
            InfoPane.setText(inp + "\n\nБаза має містити хоча б одну таблицю!");
        }
        else{
            isGUIActive = true;
            TGUI.main(dbInd, curTable.getIndex());
        }
    }//GEN-LAST:event_GoToButtonActionPerformed

